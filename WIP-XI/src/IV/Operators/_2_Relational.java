public class _2_Relational {
    public static void main(String[] args) {
        int x = 5;
        
        System.out.println("== : " + (x == 5));
        
        System.out.println("!= : " + (x !=4));
        
        System.out.println("> : " + (x > 4));
        
        System.out.println("< : " + (x < 6));
        
        System.out.println(">= : " + (x >= -1));
        
        System.out.println("<= : " + (x <= 10));
    }
}
